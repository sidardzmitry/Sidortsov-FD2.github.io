const squares = [];

//Пишим функцию CreateBord, запускаем функцию для добавления классов(картинки) в массив...
function createBoard() {
  for (let i = 0; i < layout.length; i++) {
    let square = document.createElement("div");
    container.appendChild(square);
    squares.push(square);

    if (layout[i] === 0) {
      squares[i].classList.add("pacmanFood");
    } else if (layout[i] === 1) {
      squares[i].classList.add("wall");
    } else if (layout[i] === 2) {
      squares[i].classList.add("notallow");
    } else if (layout[i] === 3) {
      squares[i].classList.add("powerpellet");
    }
  }
}

//Вызываем функцию...
createBoard();

let pacmanCurrentIndex = 490;
squares[pacmanCurrentIndex].classList.add("pacman");

function movePacman(e) {
  squares[pacmanCurrentIndex].classList.remove("pacman");

  switch (e.keyCode) {
    case 37:
      if (
        pacmanCurrentIndex % width !== 0 &&
        !squares[pacmanCurrentIndex - 1].classList.contains("wall") &&
        !squares[pacmanCurrentIndex - 1].classList.contains("notallow")
      )
      pacmanCurrentIndex -= 1;
        if (squares[pacmanCurrentIndex - 1] === squares[363])
          pacmanCurrentIndex = 391;
      break;
    case 38:
      if (
        pacmanCurrentIndex - width >= 0 &&
        !squares[pacmanCurrentIndex - width].classList.contains("wall") &&
        !squares[pacmanCurrentIndex - width].classList.contains("notallow")
      )
        pacmanCurrentIndex -= width;
      break;
    case 39:
      if (
        pacmanCurrentIndex % width < width - 1 &&
        !squares[pacmanCurrentIndex + 1].classList.contains("wall") &&
        !squares[pacmanCurrentIndex + 1].classList.contains("notallow")
      )
        pacmanCurrentIndex += 1;
        if (squares[pacmanCurrentIndex + 1] === squares[392])
          pacmanCurrentIndex = 364;
      break;
    case 40:
      if (
        pacmanCurrentIndex + width < width * width &&
        !squares[pacmanCurrentIndex + width].classList.contains("wall") &&
        !squares[pacmanCurrentIndex + width].classList.contains("notallow")
      )
        pacmanCurrentIndex += width;
      break;
  }

  squares[pacmanCurrentIndex].classList.add("pacman");
}

document.addEventListener("keyup", movePacman);

//Создаем функцию foodEaten (съеденая еда)...
function foodEaten() {
    if (squares[pacmanCurrentIndex].classList.contains('pacmanFood')) {
        music.play();
        score++;
        displayScore.innerHTML = score;
        squares[pacmanCurrentIndex].classList.remove('pacmanFood');
    }
};